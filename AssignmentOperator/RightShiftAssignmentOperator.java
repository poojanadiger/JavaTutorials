package AssignmentOperator;

public class RightShiftAssignmentOperator {
 public static void main(String[] args) {
	 int x = 5;
	 x >>= 3;
	 System.out.println(x);
	 System.out.println("101 shifted ones = 010 = 2");
	 System.out.println("010 shifted ones = 001 = 1");
	 System.out.println("001 shifted ones = 000 = 0");
 }
}
