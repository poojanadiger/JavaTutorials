package AssignmentOperator;

public class LeftshiftAssignmentOperator {
 public static void main(String[] args) {
	 int x = 5;
	 x <<= 3;
	 System.out.println(x);
	 System.out.println("101 left shifted ones = 01010 = 10");
	 System.out.println("1010 left shifted ones = 10100 = 20");
	 System.out.println("10100 left shifted ones = 101000 = 40");
 }
}
