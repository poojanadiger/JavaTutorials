package JavaKeywords;

public class FloatKeyword {
 public static void main(String[] args) {
	 float myNum = 5.75f;
	 System.out.println(myNum);
	 }
}

//The float keyword is a data type that can store fractional numbers
//from 3.4e -038 to 3.4e+038
//Note that you should end the value with an f.
