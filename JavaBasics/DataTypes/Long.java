package DataTypes;

public class Long {

	public static void main(StringDataType[] args) {
		long myNum = 150000000000L;
		System.out.println(myNum);
		System.out.println("Note that you should end the value with an L");
		System.out.println("Size of long is 8 bytes");
		System.out.println("2^64");
	}
	
}
