package Variables;

public class DeclaringVariable4 {
 public static void main(String[] args) {
	 int myNum = 15;
	 myNum = 20; //myNum is now 20
	 System.out.println(myNum);
	 System.out.println("Note that if you assign a new value to an existing variable,");
	 System.out.println("it will overwrite the previous value.");
 }
}
